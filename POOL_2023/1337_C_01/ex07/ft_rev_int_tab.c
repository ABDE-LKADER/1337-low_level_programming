void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	swap;

	i = 0;
	j = size - 1;
	while (j > 0 && i < j)
	{
		swap = tab[j];
		tab[j] = tab[i];
		tab[i] = swap;
		i++;
		j--;
	}
}
