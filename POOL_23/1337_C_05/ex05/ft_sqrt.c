int	ft_sqrt(int nb)
{
	int	sq;

	sq = 3;
	if (nb == 1)
		return (1);
	if (nb == 4)
		return (2);
	while (sq < nb / sq)
	{
		if (sq * sq == nb)
			return (sq);
	}
	return (0);
}
